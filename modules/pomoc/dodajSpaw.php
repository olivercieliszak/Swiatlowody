<center><h3>Dodawanie spawu</h3></center>
</td>
</tr>
<tr><td>1. Aby dodać nowy spaw należy spełnić następujące wymagania:
<ul>
<li>Powinna istnieć mufa (Zobacz: <a href="?modul=pomoc&co=dodajMufe" class="zmien">Dodawanie mufy</a>)</li>
<li>Co najmniej 2 kable powinny mieć mufę za punkt wspólny (za punkt początku lub końca kabla) (Zobacz: <a href="?modul=pomoc&co=dodajKabel" class="zmien">Dodawanie kabla</a>)</li>
<li>Kable powinny mieć przypisane włókna</li>
</ul></td></tr>
<tr><td>2. Wchodzimy w "Mufy" <br><img src="./img/pomoc/dodajSpaw/1.png" class="instrukcja"></td></tr>
<tr><td>3. Przy wybranym przez nas kablu wybieramy "Spawy" <br><img src="./img/pomoc/dodajSpaw/2.png" class="instrukcja"></td></tr>
<tr><td>4. Pojawiła się lista spawów. Aby dodać nowy spaw, należy wybrać "Dodaj spaw" <br><img src="./img/pomoc/dodajSpaw/3.png" class="instrukcja"></td></tr>
<tr><td>5. Pojawił nam się formularz dodawania spawu. Wybieramy kable, w których są włókna, które chcemy ze sobą zespawać. <br><img src="./img/pomoc/dodajSpaw/4.png" class="instrukcja"></td></tr>
<tr><td>6. Wybieramy tuby w obu kablach <br><img src="./img/pomoc/dodajSpaw/5.png" class="instrukcja"></td></tr>
<tr><td>7. Wybieramy kolory włókien w obu kablach <br><img src="./img/pomoc/dodajSpaw/6.png" class="instrukcja"></td></tr>
<tr><td>8. Możemy dopisać opis spawu - np. jego szacowane tłumienie lub informacje o dacie wykonania <br><img src="./img/pomoc/dodajSpaw/7.png" class="instrukcja"></td></tr>
<tr><td>9. Klikamy [Dodaj] <br><img src="./img/pomoc/dodajSpaw/8.png" class="instrukcja"></td></tr>
<tr><td>10. Spaw został dodany. Aby zobaczyć listę spawów, należy wybrać "Powrót do listy spawów". <br><img src="./img/pomoc/dodajSpaw/9.png" class="instrukcja"></td></tr>
