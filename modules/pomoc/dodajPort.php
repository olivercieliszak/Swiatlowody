<tr><td>4. Pojawiła się lista portów. Wybieramy "Dodaj port" <br><img src="./img/pomoc/dodajPort/3.png" class="instrukcja"></td></tr>
<tr><td>5. Podajemy numer portu i typ portu <br><img src="./img/pomoc/dodajPort/4.png" class="instrukcja"> <br><img src="./img/pomoc/dodajPort/5.png" class="instrukcja"></td></tr>
<tr><td>6. Wybieramy kabel, który ma przełącznice za punkt wspólny <br><img src="./img/pomoc/dodajPort/6.png" class="instrukcja"></td></tr>
<tr><td>7. Wybieramy tubę w tym kablu <br><img src="./img/pomoc/dodajPort/7.png" class="instrukcja"></td></tr>
<tr><td>8. I wybieramy włókno które ma być osiągalne na tym porcie <br><img src="./img/pomoc/dodajPort/8.png" class="instrukcja"></td></tr>
<tr><td>9. Klikamy [Dodaj] <br><img src="./img/pomoc/dodajPort/9.png" class="instrukcja"></td></tr>
