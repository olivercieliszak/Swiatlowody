<tr><td>1. Aby dodać port do przełącznicy należy spełnić następujące wymagania:
<ul>
<li>Powinna istnieć przełącznica (Zobacz: <a href="?modul=pomoc&co=dodajPrzelacznice" class="zmien">Dodawanie przełącznicy</a>)</li>
<li>Co najmniej jeden kabel powinien mieć przełącznicę za punkt wspólny (za punkt początku lub końca kabla) (Zobacz: <a href="?modul=pomoc&co=dodajKabel" class="zmien">Dodawanie kabla</a>)</li>
<li>Kable powinny mieć przypisane włókna</li></ul></td></tr>
