<center><h3>Dodawanie relacji</h3></center>
</td>
</tr>
<tr><td>1. Aby dodać relację logiczną między 2-ma punktami należy spełnić następujące wymagania:
<ul>
<li>Pomiędzy 2-ma punktami relacji logicznej powinny istnieć połączone ze sobą włókna, które nie są używane w innej relacji (może być to kabel bezpośredni lub kabel łączony w mufie)<br><img src="./img/pomoc/dodajRelacje/0.png" class="instrukcja"></li>
<li>Włókna w mufie muszą być ze sobą prawidłowo zespawane<br><img src="./img/pomoc/dodajRelacje/0A.png" class="instrukcja"></li>
</ul></td></tr>
<tr><td>2. Wchodzimy w "Relacje" <br><img src="./img/pomoc/dodajRelacje/1.png" class="instrukcja"></td></tr>
<tr><td>3. Wybieramy "Dodaj relację" <br><img src="./img/pomoc/dodajRelacje/2.png" class="instrukcja"></td></tr>
<tr><td>4. Wybieramy punkt początkowy <br><img src="./img/pomoc/dodajRelacje/3.png" class="instrukcja"></td></tr>
<tr><td>5. Wybieramy punkt końcowy <br><img src="./img/pomoc/dodajRelacje/4.png" class="instrukcja"></td></tr>
<tr><td>6. Mamy możliwość dopisania informacji związanych z daną relacją przy punkcie początkowym i przy punkcie końcowym  <br><img src="./img/pomoc/dodajRelacje/5.png" class="instrukcja"></td></tr>
<tr><td>7. W polu "Dodatkowy opis" należy wpisać opis tej relacji - np. połączenie Serwerownia - klient Kowalski <br><img src="./img/pomoc/dodajRelacje/6.png" class="instrukcja"></td></tr>
<tr><td>8. Klikamy [Dodaj] a następnie "Powrót do listy relacji"</td></tr>
<tr><td>9. Teraz należy zestawić włókna do tej relacji. W tym celu przy naszej relacji wybieramy "Włókna" <br><img src="./img/pomoc/dodajRelacje/7.png" class="instrukcja"></td></tr>
<tr><td>10. Na liście włókien wybieramy "Wyszukaj włókna". <br><img src="./img/pomoc/dodajRelacje/8.png" class="instrukcja"></td></tr>
<tr><td>11. Zostają wyszukane wszystkie możliwości zestawienia tego połączenia. Należy wybrać przycisk [Wybierz tę relację] nad interesującym nas zestawem włókien. <br><img src="./img/pomoc/dodajRelacje/9.png" class="instrukcja"></td></tr>
<tr><td>12. Włókna zostały przypisane do obsługi tej relacji. Stosowna informacja na ten temat znajdzie się również w liście spawów w mufie oraz w liście włókien w kablu. <br><img src="./img/pomoc/dodajRelacje/10.png" class="instrukcja"> <br><img src="./img/pomoc/dodajRelacje/11.png" class="instrukcja"></td></tr>
