<tr><td>2. Wchodzimy w "Przełącznice" <br><img src="./img/pomoc/dodajPrzelacznice/1.png" class="instrukcja"></td></tr>
<tr><td>3. Pojawia nam się lista wszystkich przełącznic. Wybieramy "Dodaj przełącznicę". <br><img src="./img/pomoc/dodajPrzelacznice/2.png" class="instrukcja"></td></tr>
<tr><td>4. Wybieramy w którym punkcie znajduje się przełącznica. Punkty są przedstawione w postaci "Nr punktu: Opis punktu" <br><img src="./img/pomoc/dodajPrzelacznice/3.png" class="instrukcja"></td></tr>
<tr><td>5. Możemy dopisać opis przełącznicy w polu "Opis" <br><img src="./img/pomoc/dodajPrzelacznice/4.png" class="instrukcja"></td></tr>
<tr><td>6. Klikamy [Dodaj] <br><img src="./img/pomoc/dodajPrzelacznice/5.png" class="instrukcja"></td></tr>
<tr><td>7. Na liście przełącznic pojawia się dodana przez nas przełącznica <br><img src="./img/pomoc/dodajPrzelacznice/6.png" class="instrukcja"></td></tr>
